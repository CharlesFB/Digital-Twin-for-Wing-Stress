    private List<ForcePoint> initialForces; // 初始受力点列表

    // 创建线性可视化应力变化
    void LinearForce()
    {
        Vector3[] vertices = mesh.vertices;
        float[] forces = new float[vertices.Length];

        initialForces = new List<ForcePoint>();
        initialForces.Add(new ForcePoint(fi1, fv1));
        initialForces.Add(new ForcePoint(fi2, fv2));
        initialForces.Add(new ForcePoint(fi3, fv3));

        Dictionary<float, bool> visited = new Dictionary<float, bool>(); // 标记高度是否已被处理
        //计算每个高度的受力值
        Dictionary<float, float> heightToForce = new Dictionary<float, float>();

        // 记录每个高度(y)的顶点索引
        Dictionary<float, List<int>> heightToVertexIndices = new Dictionary<float, List<int>>();
        for (int i = 0; i < vertices.Length; i++)
        {
            float y = vertices[i].y;
            if (!heightToVertexIndices.ContainsKey(y))
            {
                heightToVertexIndices[y] = new List<int>();
                visited[y] = false;
                heightToForce[y] = 0;
            }
            heightToVertexIndices[y].Add(i);
        }

        Queue<(float height, float currentStep, float currentForce)> queue = new Queue<(float, float, float)>();
        foreach (var forcePoint in initialForces)
        {
            float y = vertices[forcePoint.vertexIndex].y;
            queue.Enqueue((y , initialStep, forcePoint.forceValue));
      
            visited[y] = true;
            heightToForce[y] = forcePoint.forceValue;
        }

        float h1 = vertices[fi1].y;
        float h2 = vertices[fi2].y;
        float h3 = vertices[fi3].y;

        while (queue.Count > 0)
        {
            var (height, currentStep, currentForce) = queue.Dequeue();

            if (currentStep <= 0.01f) continue; // 如果步长太小，则停止传播

            // 遍历当前高度附近的所有高度组
            foreach (var kv in heightToVertexIndices)
            {
                if (visited[kv.Key]) continue; //跳过已处理的高度
                if (kv.Key > vertices[fi1].y && kv.Key < vertices[fi3].y)
                {
                    if (kv.Key > h1 && kv.Key < h2) // Part1线性插值
                    {
                        heightToForce[kv.Key] = fv1 + (fv2 - fv1) * (kv.Key - h1) / (h2 - h1);
                    }
                    else if (kv.Key > h2 && kv.Key < h3) // Part2
                    {
                        heightToForce[kv.Key] = fv2 + (fv3 - fv2) * (kv.Key - h2) / (h3 - h2);
                    }
                    visited[kv.Key] = true;
                    continue; 
                }
                float distance = Mathf.Abs(kv.Key - height);
                if (distance <= currentStep)
                {
                    float propagatedForce = currentForce * (1 - distance / currentStep); // 距离越远，力越小
                    heightToForce[kv.Key] = Mathf.Max(heightToForce[kv.Key], propagatedForce);
                    visited[kv.Key] = true;
                    queue.Enqueue((kv.Key, currentStep * decayRate, propagatedForce));
                }
            }
        }

        foreach (var kvp in heightToVertexIndices)
        {
            float height = kvp.Key;
            float forceValue = heightToForce[height];

            foreach (int vertexIndex in kvp.Value)
            {
                forces[vertexIndex] = forceValue;
            }
        }

        ApplyForceToColors(mesh, forces);
    }

    void GetForce()
    {
        Vector3[] vertices = mesh.vertices;

        float[] forces = new float[vertices.Length]; // 存储每个顶点的力值
        bool[] visited = new bool[vertices.Length]; // 标记顶点是否已被处理

        // 初始化：给定的3个初始点的力
        Queue<(int index, float currentStep, float currentForce)> queue = new Queue<(int, float, float)>();
        initialForces = new List<ForcePoint>();
        initialForces.Add(new ForcePoint(fi1, fv1));
        initialForces.Add(new ForcePoint(fi2, fv2));
        initialForces.Add(new ForcePoint(fi3, fv3));

        foreach (var forcePoint in initialForces)
        {
            queue.Enqueue((forcePoint.vertexIndex, initialStep, forcePoint.forceValue));
            forces[forcePoint.vertexIndex] = forcePoint.forceValue;
            visited[forcePoint.vertexIndex] = true;
        }

        // 开始基于步长传播力值
        while (queue.Count > 0)
        {
            var (currentIndex, currentStep, currentForce) = queue.Dequeue();

            if (currentStep <= 0.01f) continue; // 如果步长太小，则停止传播

            // 遍历当前点附近的所有顶点
            for (int i = 0; i < vertices.Length; i++)
            {
                if (visited[i]) continue; // 跳过已处理的顶点

                float distance = Vector3.Distance(vertices[currentIndex], vertices[i]);
                if (distance <= currentStep) // 在当前步长范围内
                {
                    float propagatedForce = currentForce * (1 - distance / currentStep); // 距离越远，力越小
                    forces[i] = Mathf.Max(forces[i], propagatedForce); // 更新力值（取较大值）
                    visited[i] = true; // 标记为已访问

                    // 将该点加入队列，继续传播
                    queue.Enqueue((i, currentStep * decayRate, propagatedForce));
                }
            }
        }

        // 输出力值或进一步处理，比如应用顶点颜色
        ApplyForceToColors(mesh, forces);
    }

    [Header("Scales")]
    public float downLimit = -5f;
    public float upLimit = 200f;

    float UniformForce(float f)
    {
        if (f <= downLimit) return 0;
        if (f >= upLimit) return 1;
        if (upLimit == 0) return 1;
        return (f - downLimit) / upLimit;
    }

    // 将力值映射到颜色，方便可视化
    void ApplyForceToColors(Mesh mesh, float[] forces)
    {
        Color[] colors = new Color[forces.Length];
        float maxForce = Mathf.Max(forces);

        for (int i = 0; i < forces.Length; i++)
        {
            //Debug.Log(forces[i]);
            float t = UniformForce(forces[i]); // 归一化
            colors[i] = GetGradientColor(t);
        }

        mesh.SetColors(colors); // 应用颜色到Mesh

        //foreach (Color color in mesh.colors)
        //{
        //    Debug.Log(color);
        //}

        // 计算法线（可选）
        mesh.RecalculateNormals();
    }

    // 根据归一化的受力值返回渐变颜色
    Color GetGradientColor(float t)
    {
        if (t < 0.33f) // 深红色到黄色
        {
            float lerp = t / 0.33f;
            return Color.Lerp(Color.blue, Color.green, lerp);
        }
        else if (t < 0.66f) // 黄色到绿
        {
            float lerp = (t - 0.33f) / 0.33f;
            return Color.Lerp(Color.green, Color.yellow, lerp);
        }
        else // 绿到蓝色
        {
            float lerp = (t - 0.66f) / 0.34f;
            return Color.Lerp(Color.yellow, new Color(0.5f, 0f, 0f), lerp);
        }
    }

